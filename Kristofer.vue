  async mounted() {
    await this.$loadScript('/js/webflow.js')

    const trigger = new ScrollTrigger()
    trigger.add('[data-trigger]')

    document.title = 'Kristofer Guldvarg'

    window.addEventListener('scroll', this.onScroll, { passive: true })
